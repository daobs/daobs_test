package com.spring.seed.vo;

public class SeedVO {
	private int seedno;
	private String seed_writer;
	private String seed_title;
	private String seed_content;
	private int seed_viewcnt;
	public int getSeedno() {
		return seedno;
	}
	public void setSeedno(int seedno) {
		this.seedno = seedno;
	}
	public String getSeed_writer() {
		return seed_writer;
	}
	public void setSeed_writer(String seed_writer) {
		this.seed_writer = seed_writer;
	}
	public String getSeed_title() {
		return seed_title;
	}
	public void setSeed_title(String seed_title) {
		this.seed_title = seed_title;
	}
	public String getSeed_content() {
		return seed_content;
	}
	public void setSeed_content(String seed_content) {
		this.seed_content = seed_content;
	}
	public int getSeed_viewcnt() {
		return seed_viewcnt;
	}
	public void setSeed_viewcnt(int seed_viewcnt) {
		this.seed_viewcnt = seed_viewcnt;
	}
	
}
